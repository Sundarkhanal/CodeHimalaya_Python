#empty tuple
tuple = ()
print(tuple)

#tuple with elements
tuple = (1,2,3,4,5,6,7,8,9)
print(tuple)


#tuple with different datatypes
tuple = (1,"Tuple",3.7)
print(tuple)

#Nested tuple
tuple = ("Cat", [100,34,56], (1,2,3))
print(tuple)

#accessing tuple item
mytuple = ("apple","banana","Cherry")
print(mytuple[1])
print(mytuple[1])
